package com.bridgelabz;

/*
      1. Start
      2. Declare variables for each primitive data type: byte, short, int,long, float, double, char, boolean.
      3. Print the default values of each variable.
      4. End
 */

public class DefaultValues {
    static byte defaultValueByte;
    static short defaultValueShort;
    static int defaultValueInt;
    static long defaultValueLong;
    static float defaultValueFloat;
    static double defaultValueDouble;
    static char defaultValueChar;
    static boolean defaultValueBoolean;
    public static void main(String[] args) {
        System.out.println("Default Value of byte: " + defaultValueByte);
        System.out.println("Default Value of short: " + defaultValueShort);
        System.out.println("Default Value of int: " + defaultValueInt);
        System.out.println("Default Value of long: " + defaultValueLong);
        System.out.println("Default Value of float: " + defaultValueFloat);
        System.out.println("Default Value of double: " + defaultValueDouble);
        System.out.println("Default Value of char: " + defaultValueChar);
        System.out.println("Default Value of boolean: " + defaultValueBoolean);
    }
}
